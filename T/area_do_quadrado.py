# 6 faça um programa que calcule a area de um quadrado, em seguida mostre o dobro desta area para o usuario
base = float(input('informe a base do quadrado:'))
altura = float(input('Informe a altura do quadrado:'))
area = base * altura
dobro = 2 * area
print(f'a area desse quadrado é {area} e o dobro dessa area é {dobro}')
n = int(input('Digite um numero:'))
dobro = 2 * n
triplo = 3 * n
raiz_quadrada = n ** (1/2)
print(f'O dobro de {n} vale {dobro}')
print(f'O triplo de {n} vale {triplo}')
print(f' A raiz quadrada de {n} é igual a {raiz_quadrada:.2f}') # exemplo de formatacão de casas decimais de um float



